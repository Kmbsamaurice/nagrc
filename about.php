								<h4 style="color: rgb(56, 18, 56);">History</h4>
								<p>The National Animal Genetic Resources Centre and Data Bank (NAGRC & DB) formally the Animal Breeding Centre (ABC) was established in 1960 as an Artificial Breeding Centre, mainly to offer artificial Insemination (AI) services to the dairy farmers.</p>
                                
                                <p>It was also charged with improvement of indigenous cattle through selective breeding and crossbreeding the indigenous cattle with imported exotic bulls.</p>
                                
                                <p>ABC had been under the Department of Animal Production and Marketing in the Ministry of Agriculture, Animal Industry and Fisheries (MAAIF) until May 2001, when it was established into a body corporate (NAGRC&DB) under the Animal Breeding Act, 2001.
                                The National Animal Genetic Resources Centre and Data Bank had its mandate expanded and charged with spearheading all breeding activities in the country and mandated to operate on commercial basis in discharging its functions and also carry out development activities which will be funded by government..
                                Additionally, for sustainability, the centre has become increasing commercialised. Through the recently ended Cattle Breeding Project, now field A.I. services are operating on the principles of increased cost recovery and gradual privatisation.
                                The civil strife of the 1970’s and early ‘80s adversely affected efforts of livestock improvement in the country. There have, however, been several efforts by the Government of Uganda and its partners in development to reverse the harm done.</p>
                                
                                <p>The development path included the DANIDA supported Master Plan for the Dairy Sector of 1992, that recognised the need for a coherent and comprehensive policy to guide all stakeholders in the improvement and conservation of the national AnGR.
                                Again, under the auspices of DANIDA, a Cattle Breeding Project was funded and implemented with the production of a number of documents that spell out the institutional and legal frameworks for livestock breeding in Uganda.</p>
                                
                                <p>During the month of June, 2003, seven farms were officially handed over to NAGRC & DB by MAAIF to facilitate breeding activities in the Country. The farms handed over are Njeru, Rubona, Nshaara, Ruhengere, Kasolwe, LES and Bulago stock farm.</p>
                                
                                
